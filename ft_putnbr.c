#include "ft_printf.h"

void	ft_putnbr(int nb, int *len)
{
	long long	n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-', len);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10, len);
		ft_putnbr(n % 10, len);
	}
	else
		ft_putchar(n + '0', len);
}
